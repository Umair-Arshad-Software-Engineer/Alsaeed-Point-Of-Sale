                    : FontStyle.normal,
                fontWeight: text.contains('auto-generated')
                    ? FontWeight.normal
                    : FontWeight.w600,
              ),
            ),
          ),
        ],
      ),
    );
  }

  Widget _buildDropdownField<T>({
    required T? value,
    required List<dynamic> items,
    required String Function(dynamic) itemLabel,
    required T? Function(dynamic) itemValue,
    required String hint,
    required ValueChanged<T?> onChanged,
  }) {
    return Container(
      padding: const EdgeInsets.symmetric(horizontal: 4),
      decoration: BoxDecoration(
        color: _canvas,
        borderRadius: BorderRadius.circular(8),
        border: Border.all(color: _border),
      ),
      child: DropdownButton<T>(
        value: value,
        isExpanded: true,
        hint: Text(
          hint,
          style: const TextStyle(
            color: _textSecondary,
            fontSize: 13.5,
          ),
        ),
        underline: const SizedBox(),
        items: [
          DropdownMenuItem<T>(
            value: null,
            child: Text(
              'None',
              style: TextStyle(
                fontSize: 13.5,
                color: _textSecondary,
              ),
            ),
          ),
          ...items.map((item) {
            return DropdownMenuItem<T>(
              value: itemValue(item),
              child: Text(
                itemLabel(item),
                style: const TextStyle(
                  fontSize: 13.5,
                  color: _textPrimary,
                ),
              ),
            );
          }),
        ],
        onChanged: onChanged,
        icon: const Icon(Icons.arrow_drop_down, color: _textSecondary),
        dropdownColor: _surface,
        elevation: 4,
        style: const TextStyle(
          fontSize: 13.5,
          color: _textPrimary,
        ),
      ),
    );
  }

  Widget _buildTextField({
    required TextEditingController controller,
    required String hint,
    required IconData icon,
    TextInputType? keyboardType,
    int maxLines = 1,
    bool enabled = true,
    String? Function(String?)? validator,
  }) {
    return TextFormField(
      controller: controller,
      keyboardType: keyboardType,
      maxLines: maxLines,
      enabled: enabled,
      validator: validator,
      style: TextStyle(
        fontSize: 13.5,
        color: enabled ? _textPrimary : _textSecondary,
      ),
      decoration: InputDecoration(
        hintText: hint,
        hintStyle:
        const TextStyle(color: _textSecondary, fontSize: 13.5),
        prefixIcon: maxLines == 1 && enabled
            ? Icon(icon, size: 17, color: _textSecondary)
            : null,
        filled: true,
        fillColor: enabled ? _canvas : _canvas.withOpacity(0.5),
        contentPadding: EdgeInsets.symmetric(
          horizontal: 14,
          vertical: maxLines > 1 ? 14 : 13,
        ),
        border: OutlineInputBorder(
          borderRadius: BorderRadius.circular(8),
          borderSide: const BorderSide(color: _border),
        ),
        enabledBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(8),
          borderSide: const BorderSide(color: _border),
        ),
        focusedBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(8),
          borderSide: const BorderSide(color: _emerald, width: 1.5),
        ),
        errorBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(8),
          borderSide: const BorderSide(color: _rose),
        ),
        focusedErrorBorder: OutlineInputBorder(
          borderRadius: BorderRadius.circular(8),
          borderSide: const BorderSide(color: _rose, width: 1.5),
        ),
        errorStyle: const TextStyle(fontSize: 11.5, color: _rose),
      ),
    );
  }

