  final _barcodeController = TextEditingController();

  int? _selectedCategoryId;
  int? _selectedBrandId;
  int? _selectedUnitId;
  bool _barcodeAutoGenerated = true;
  bool _isLoading = false;

  // New variables for item code and barcode generation
  String _itemCode = '';
  String _generatedBarcode = '';

  // Net rate calculation
  double _netRate = 0.0;

  bool get _isEditing => widget.product != null;

  @override
  void initState() {
    super.initState();

    // Load dropdown data and generate codes
    WidgetsBinding.instance.addPostFrameCallback((_) async {
      await _loadDropdownData();
      _generateCodes();
    });

    if (_isEditing) {
      // Pre-fill editing data
      _nameController.text = widget.product!.name;
      _purchaseRateController.text = widget.product!.purchaseRate.toString();
      _saleRateController.text = widget.product!.saleRate.toString();
      _taxPercentageController.text = widget.product!.taxPercentage.toString();
      _openingQtyController.text = widget.product!.openingQty.toString();
      _pctCodeController.text = widget.product!.pctCode ?? '';
      _descriptionController.text = widget.product!.description;
      _selectedCategoryId = widget.product!.categoryId;
      _selectedBrandId = widget.product!.brandId;
      _selectedUnitId = widget.product!.unitId;
      _barcodeAutoGenerated = widget.product!.barcodeAutoGenerated;

      // Use existing codes for editing
      _itemCode = widget.product!.itemCode;
      if (!_barcodeAutoGenerated && widget.product!.barcode != null) {
        _barcodeController.text = widget.product!.barcode!;
        _generatedBarcode = widget.product!.barcode!;
      } else {
        _generatedBarcode = widget.product!.barcode ?? '';
        _barcodeController.text = _generatedBarcode;
      }

      // Calculate net rate for editing
      _calculateNetRate();
    }

    // Add listeners to recalculate net rate when sale rate or tax changes
    _saleRateController.addListener(_calculateNetRate);
    _taxPercentageController.addListener(_calculateNetRate);
  }

  Future<void> _loadDropdownData() async {
    final categoryController = context.read<CategoryController>();
    final brandController = context.read<BrandController>();
    final unitController = context.read<UnitController>();

    await Future.wait([
      categoryController.fetchCategories(),
      brandController.fetchBrands(),
      unitController.fetchUnits(),
    ]);
  }

  // ── Code Generation Methods ─────────────────────────────────────────────────

  void _generateCodes() {
    if (_isEditing) return; // Don't generate new codes when editing

    // Get the next item code from the product controller
    final productController = context.read<ProductController>();
    _itemCode = _generateNextItemCode(productController.products);

    // Generate barcode if auto-generated
    if (_barcodeAutoGenerated) {
      _generatedBarcode = _generateBarcode();
      _barcodeController.text = _generatedBarcode;
    }

    setState(() {});
  }

  String _generateNextItemCode(List<Product> products) {
    // If no products exist, start with 1
    if (products.isEmpty) {
      return 'PRD-0001';
    }

    // Find the highest item code number
    int maxNumber = 0;
    for (var product in products) {
      // Extract number from item code (assuming format PRD-XXXX)
      final parts = product.itemCode.split('-');
      if (parts.length == 2) {
        final number = int.tryParse(parts[1]);
        if (number != null && number > maxNumber) {
          maxNumber = number;
        }
      }
    }

    // Generate next number with leading zeros
    final nextNumber = maxNumber + 1;
    return 'PRD-${nextNumber.toString().padLeft(4, '0')}';
  }

  String _generateBarcode() {
    // Generate a unique barcode based on current timestamp
    // You can customize this based on your requirements
    final timestamp = DateTime.now().millisecondsSinceEpoch;
    final random = DateTime.now().microsecond % 10000;
    return 'BAR${timestamp.toString().substring(timestamp.toString().length - 8)}${random.toString().padLeft(4, '0')}';
  }

  // ── Net Rate Calculation ────────────────────────────────────────────────────

  void _calculateNetRate() {
    setState(() {
      final saleRate = double.tryParse(_saleRateController.text) ?? 0.0;
      final taxPercentage = double.tryParse(_taxPercentageController.text) ?? 0.0;
      _netRate = saleRate + (saleRate * taxPercentage / 100);
    });
  }

  @override
  void dispose() {
    _nameController.dispose();
    _purchaseRateController.dispose();
    _saleRateController.dispose();
    _taxPercentageController.dispose();
    _openingQtyController.dispose();
    _pctCodeController.dispose();
    _descriptionController.dispose();
    _barcodeController.dispose();
    // Remove listeners
    _saleRateController.removeListener(_calculateNetRate);
    _taxPercentageController.removeListener(_calculateNetRate);
    super.dispose();
  }

