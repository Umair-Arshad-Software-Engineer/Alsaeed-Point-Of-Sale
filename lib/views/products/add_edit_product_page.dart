  Future<void> _saveProduct() async {
    if (!_formKey.currentState!.validate()) return;
    setState(() => _isLoading = true);

    final pc = context.read<ProductController>();

    final bool success;
    if (_isEditing) {
      success = await pc.updateProduct(
        id: widget.product!.id,
        name: _nameController.text.trim(),
        purchaseRate: double.parse(_purchaseRateController.text),
        saleRate: double.parse(_saleRateController.text),
        taxPercentage: _taxPercentageController.text.isEmpty
            ? 0
            : double.parse(_taxPercentageController.text),
        openingQty: _openingQtyController.text.isEmpty
            ? 0
            : double.parse(_openingQtyController.text),
        categoryId: _selectedCategoryId,
        brandId: _selectedBrandId,
        unitId: _selectedUnitId,
        pctCode: _pctCodeController.text.trim().isEmpty
            ? null
            : _pctCodeController.text.trim(),
        description: _descriptionController.text.trim(),
        barcode: _barcodeController.text.trim().isEmpty
            ? null
            : _barcodeController.text.trim(),
        barcodeAutoGenerated: _barcodeAutoGenerated,
      );
    } else {
      success = await pc.createProduct(
        name: _nameController.text.trim(),
        purchaseRate: double.parse(_purchaseRateController.text),
        saleRate: double.parse(_saleRateController.text),
        taxPercentage: _taxPercentageController.text.isEmpty
            ? 0
            : double.parse(_taxPercentageController.text),
        openingQty: _openingQtyController.text.isEmpty
            ? 0
            : double.parse(_openingQtyController.text),
        categoryId: _selectedCategoryId,
        brandId: _selectedBrandId,
        unitId: _selectedUnitId,
        pctCode: _pctCodeController.text.trim().isEmpty
            ? null
            : _pctCodeController.text.trim(),
        description: _descriptionController.text.trim(),
        barcode: _barcodeController.text.trim().isEmpty
            ? null
            : _barcodeController.text.trim(),
        barcodeAutoGenerated: _barcodeAutoGenerated,
      );
    }

    setState(() => _isLoading = false);
    if (!mounted) return;

    ScaffoldMessenger.of(context).showSnackBar(SnackBar(
      content: Text(success
          ? (_isEditing ? 'Product updated' : 'Product added to inventory')
          : (pc.errorMessage ?? 'Failed to save product')),
      backgroundColor: success ? _emerald : _rose,
      behavior: SnackBarBehavior.floating,
      shape:
      RoundedRectangleBorder(borderRadius: BorderRadius.circular(8)),
    ));

    if (success) Navigator.pop(context);
  }
